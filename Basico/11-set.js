conjunto.delete("Palabra2") // Elimina un elemento especifico
conjunto.has("Palabra") // Verifica si existe un elemento en el conjunto
conjunto.size // Tamanio del conjunto
let arreglo = Array.from(conjunto) // Convierte un conjunto en un arreglo
